package io.rybalkinsd.kotlinbootcamp;

public class PointJ {
    private int x;
    private int y;

    public PointJ(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
